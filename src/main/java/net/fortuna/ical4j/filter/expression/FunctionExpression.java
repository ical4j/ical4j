package net.fortuna.ical4j.filter.expression;

import java.time.temporal.Temporal;
import java.util.Objects;
import java.util.function.Function;

public class FunctionExpression implements LiteralExpression<Function<String, Temporal>> {

    private final Function<String, Temporal> value;

    public FunctionExpression(Function<String, Temporal> value) {
        this.value = value;
    }

    @Override
    public Function<String, Temporal> getValue() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        var that = (FunctionExpression) o;
        return Objects.equals(value, that.value);
    }

    @Override
    public int hashCode() {
        return Objects.hash(value);
    }
}
